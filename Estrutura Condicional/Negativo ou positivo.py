# Entrada do usuário com um valor.
numero = int(input('Digite um valor numerico.: '))

# Verificação. Onde "numero" é maior que "zero" ou menor. Condição composta ou binária.
if numero < 0:
    print(f'O numero {numero} é Negativo.')
else:
    print(f'O numero {numero} é Positivo.')
