# Entrada pelo usuário.
num = int(input('Digite um número.: '))

# Condição binária, verifica se número é par ou impar. 
if num % 2 == 0:
    print(f'O número {num} é "PAR".')
else:
    print(f'O número {num} NÃO É PAR.')
